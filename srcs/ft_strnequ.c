#include <string.h>

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	if (s1 == s2 || !n)
		return (1);
	while (n && s1 && s2 && *s1 && *s1++ == *s2++)
		--n;
	return (!n || (s1 && s2 && *s1 == *s2));
}
