#include <stdint.h>

int	ft_strcmp(char const *s1, char const *s2)
{
	while (s1 && s2 && *s1 && *(uint8_t *)s1 == *(uint8_t *)s2)
	{
		++s1;
		++s2;
	}
	return (*(uint8_t *)s1 - *(uint8_t *)s2);
}
